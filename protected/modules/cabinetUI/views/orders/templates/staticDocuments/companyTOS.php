<p>3.4. Порядок получения согласия на обработку персональных данных клиентов.</p>

<p>В разделе 1статьи 9 Федерального закона "О персональных данных" №152-ФЗ, определено, что субъект персональных данных принимает решение о предоставлении его персональных данных и дает согласие на их обработку свободно, своей волей и в своем интересе.</p>

<p>3.4.1. Согласие на обработку персональных данных должно быть конкретным, информированным и сознательным. Согласие на обработку персональных данных может быть дано субъектом персональных данных или его представителем в любой позволяющей подтвердить факт его получения форме, если иное не установлено федеральным законом. В случае получения согласия на обработку персональных данных от представителя субъекта персональных данных полномочия данного представителя на дачу согласия от имени субъекта персональных данных проверяются оператором.</p>

<p>3.4.2. С целью явного информирования клиентов о составе и целях обработки их персональных данных соответствующие положения должны быть включены в Договора Общества как с физическими и юридическими. В обязанности менеджеров входит ознакомление клиентов Общества с этими положениями при подписании договоров и соответствующих Приложений к ним.</p>

<p>3.4.3. Письменное согласие клиента на обработку своих персональных данных должно включать в себя:</p>
<ul>
<li>фамилию, имя, отчество, адрес субъекта персональных данных, номер основного документа, удостоверяющего его личность, сведения о дате выдачи указанного документа и выдавшем его органе</li>
<li>наименование (фамилию, имя, отчество) и адрес оператора, получающего согласие субъекта персональных данных</li>
<li>цель обработки персональных данных</li>
<li>перечень персональных данных, на обработку которых дается согласие субъекта персональных данных</li>
<li>перечень действий с персональными данными, на совершение которых дается согласие, общее описание используемых оператором способов обработки персональных данных</li>
