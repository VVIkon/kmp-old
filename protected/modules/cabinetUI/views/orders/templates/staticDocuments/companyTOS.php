<p>6)&nbsp;<b>предоставление персональных данных</b>&nbsp;- действия, направленные на раскрытие персональных данных определенному лицу или определенному кругу лиц; ООО "КМП групп" ПОЛОЖЕНИЕ об обработке персональных данных клиентов в ООО "КМП групп" 4</p>

