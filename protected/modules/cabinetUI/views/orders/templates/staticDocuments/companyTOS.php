<p>2. Основные понятия и состав персональных данных</p>

<p>В настоящем Положении используются следующие основные понятия, определения которых даны статье 3 закона №152-ФЗ:</p>

<p>1)&nbsp;<b>персональные данные</b>&nbsp;- любая информация, относящаяся к прямо или косвенно определенному или определяемому физическому лицу (субъекту персональных данных);</p>

<p>2)&nbsp;<b>оператор</b>&nbsp;- государственный орган, муниципальный орган, юридическое или физическое лицо, самостоятельно или совместно с другими лицами организующие и (или) осуществляющие обработку персональных данных, а также определяющие цели обработки персональных данных, состав персональных данных, подлежащих обработке, действия (операции), совершаемые с персональными данными;</p>

<p>3)&nbsp;<b>обработка персональных данных</b>&nbsp;- любое действие (операция) или совокупность действий (операций), совершаемых с использованием средств автоматизации или без использования таких средств с персональными данными, включая сбор, запись, систематизацию, накопление, хранение, уточнение (обновление, изменение), извлечение, использование, передачу (распространение, предоставление, доступ), обезличивание, блокирование, удаление, уничтожение персональных данных;</p>

<p>4)&nbsp;<b>автоматизированная обработка персональных данных</b>&nbsp;- обработка персональных данных с помощью средств вычислительной техники;</p>

