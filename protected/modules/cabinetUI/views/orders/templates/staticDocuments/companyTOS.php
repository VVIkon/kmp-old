<p>Об обработке персональных данных клиентов ООО "КМП групп"</p>

<h3>Общие положения</h3>

<p>1.1. Цель разработки Положения- определение порядка обработки персональных данных клиентов Общества; обеспечение защиты прав и свобод клиентов Общества при обработке их персональных данных, а также установление ответственности должностных лиц, имеющих доступ к персональным данным клиентов Общества, за невыполнение требований норм, регулирующих обработку и защиту персональных данных.</p>

<p>1.2. Настоящее Положение разработано в соответствии с требованиями Федеральных законов "О персональных данных" №152-ФЗ от 27.07.2006, "Об информации, информационных технологиях и о защите информации" № 149-ФЗ от 27.07.2006 г., Постановления Правительства Российской Федерации от 17.11.2007г. №781 "Об утверждении Положения об обеспечении безопасности персональных данных при их обработке в информационных системах персональных данных".</p>

<p>1.3. Персональные данные относятся к категории конфиденциальной информации. Сотрудники, получившие доступ к персональным данным, обязаны не раскрывать третьим лицам и не распространять персональные данные без согласия субъекта персональных данных, если иное не предусмотрено федеральным законом.</p>

<p>1.4. Порядок ввода в действие и изменения Положения.</p>

<p>1.4.1.Настоящее Положение вступает в силу с момента его утверждения приказом Генерального директора Общества и действует бессрочно, до замены его новым Положением.</p>

<p>1.4.2.Все изменения в Положение вносятся приказом.</p>

