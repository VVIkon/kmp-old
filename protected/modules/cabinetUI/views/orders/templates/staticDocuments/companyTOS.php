<p>3.&nbsp;<b>Обработка персональных данных</b>&nbsp;Под обработкой персональных данных клиента понимаются действия (операции) с персональными данными, включая сбор, систематизацию, накопление, хранение, уточнение (обновление, изменение), использование, распространение, в том числе передача, обезличивание, блокирование, уничтожение персональных данных.</p>

<p>3.2. Принципы и условия обработки персональных данных в полной мере определны статьей 5 Федерального закона "О персональных данных" №152-ФЗ.</p>

