<p>8)&nbsp;<b>уничтожение персональных данных</b>&nbsp;- действия, в результате которых становится невозможным восстановить содержание персональных данных в информационной системе персональных данных и (или) в результате которых уничтожаются материальные носители персональных данных;</p>

