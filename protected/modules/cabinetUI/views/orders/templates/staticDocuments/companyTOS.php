<p>3.4. Передача персональных данных клиента третьим лицам</p>

<p>Разделом 3 статьи 6 Федерального закона "О персональных данных" №152-ФЗ определено что Оператор вправе поручить обработку персональных данных другому лицу с согласия субъекта персональных данных, если иное не предусмотрено федеральным законом, на основании заключаемого с этим лицом договора, в том числе государственного или муниципального контракта, либо путем принятия государственным или муниципальным органом соответствующего акта (далее - поручение оператора). Лицо, осуществляющее обработку персональных данных по поручению оператора, обязано соблюдать принципы и правила обработки персональных данных, предусмотренные настоящим Федеральным законом. В поручении оператора должны быть определены перечень действий (операций) с персональными данными, которые будут совершаться лицом, осуществляющим обработку персональных данных, и цели обработки, должна быть установлена обязанность такого лица соблюдать конфиденциальность персональных данных и обеспечивать безопасность персональных данных при их обработке, а также должны быть указаны требования к защите обрабатываемых персональных данных в соответствии со статьей 19 настоящего Федерального закона.</p>

<p>3.4.1. При передаче персональных данных клиента необходимо соблюдать следующие требования:</p>
<ul>
<li>не сообщать персональные данные клиента третьей стороне без письменного согласия самого клиента, за исключением случаев, когда это необходимо в целях предупреждения угрозы жизни и здоровью клиента, а также в случаях, установленных федеральным законом</li>
<li>не сообщать персональные данные клиента в коммерческих целях без его письменного согласия</li>
<li>предупредить лиц, получающих персональные данные клиента, о том, что эти данные могут быть использованы лишь в целях, для которых они сообщены, и требовать от этих лиц подтверждения того, что это правило соблюдено</li>
<li>разрешать доступ к персональным данным клиентов только специально уполномоченным лицам (п.4.1.1. настоящего Положения), при этом указанные лица должны иметь право получать только те персональные данные клиента, которые необходимы для выполнения конкретных функций</li>
</ul>

<p>3.4.2. Передача персональных данных от держателя или его представителей внешнему потребителю может допускаться в минимальных объемах и только в целях выполнения задач, соответствующих объективной причине сбора этих данных.</p>

<p>3.4.3. При передаче персональных данных клиента потребителям (в том числе и в коммерческих целях) за пределы организации работодатель не должен сообщать эти данные третьей стороне без письменного согласия клиента, за исключением случаев, когда это необходимо в целях предупреждения угрозы жизни и здоровью клиента или в случаях, установленных федеральным законом.</p>

